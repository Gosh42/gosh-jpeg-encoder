        public static Dictionary<byte, byte> GetCodeLengthDict(bool isLuminance, bool isDC)
        {
            byte[][] table = new byte[0][];
            if (isLuminance && isDC)
            {
                table = HuffmanTable_LumDC;
            }
            else if (isLuminance && !isDC)
            {
                table = HuffmanTable_LumAC;
            } /*
            else if (!isLuminance && isDC)
            {
                table = HuffmanTable_ChromDC;
            }
            else
            {
                table = HuffmanTable_ChromAC;
            }*/
            Dictionary<byte, byte> dict = new Dictionary<byte, byte>();

            for (int i = 0; i < table.Length; i++)
            {
                for (int j = 0; j < table[i].Length; j++)
                {
                    dict.Add(table[i][j], (byte)(i + 1));
                }
            }

            return dict;
        }
